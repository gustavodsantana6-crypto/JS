const somar = (a, b) => {
    return a + b
}

const subtrair = (a, b) => {
    return a - b
}

const multplicar = (a, b) => {
    return a * b
}

const dividir = (a, b) => {
    return a / b
}

const soma = somar(2, 3)
const subtracao = subtrair(2, 3)
const Multiplicação = multplicar(2, 3)
const divisao = dividir(2, 3)

console.log("Soma: ", soma)
console.log("Subtração: ", subtracao)
console.log("Multiplicação: ", Multiplicação)
console.log("Divisão: ", divisao)